def clear_cell_content(cell):
    """셀의 모든 내용(단락, 이미지)을 제거하고 빈 단락 하나만 남김"""
    for p in cell.paragraphs[1:]:
        p._element.getparent().remove(p._element)
    # 첫 번째 단락의 내용 제거
    first_p = cell.paragraphs[0]
    for run in first_p.runs:
        run._element.getparent().remove(run._element)
    first_p.text = ""
    # 인라인 이미지 제거
    ns = {'w': 'http://schemas.openxmlformats.org/wordprocessingml/2006/main',
          'wp': 'http://schemas.openxmlformats.org/drawingml/2006/wordprocessingDrawing'}
    for drawing in cell._tc.findall('.//w:drawing', ns):
        drawing.getparent().remove(drawing)


def add_text_to_cell(cell, text, font_name="맑은 고딕", font_size=Pt(10), bold=False):
    """셀에 서식화된 텍스트 추가"""
    lines = text.split('\n')
    first = True
    for line in lines:
        if first:
            p = cell.paragraphs[0]
            first = False
        else:
            p = cell.add_paragraph()

        p.paragraph_format.space_before = Pt(1)
        p.paragraph_format.space_after = Pt(1)
        p.paragraph_format.line_spacing = Pt(13)

        run = p.add_run(line)
        run.font.name = font_name
        run.font.size = font_size
        run.font.bold = bold

        # 한글 폰트 설정
        from docx.oxml.ns import qn
        rPr = run._element.get_or_add_rPr()
        rFonts = rPr.find(qn('w:rFonts'))
        if rFonts is None:
            rFonts = etree.SubElement(rPr, qn('w:rFonts'))
        rFonts.set(qn('w:eastAsia'), font_name)


def generate_note(project_num):
    """특정 프로젝트 번호의 연구노트 생성"""
    proj = PROJECTS[project_num]
    content = CONTENT[project_num]

    # 템플릿 복사
    doc = Document(TEMPLATE_PATH)

    # === 테이블 0: 헤더 ===
    table0 = doc.tables[0]
    # No. 변경
    cell_no = table0.rows[0].cells[2]
    for p in cell_no.paragraphs:
        for run in p.runs:
            if "2025-R" in run.text:
                run.text = f"No. {proj['no']}"

    # === 테이블 1: 메타정보 ===
    table1 = doc.tables[1]
    # 과제명 변경 (행0, 셀1~5 병합) — 첫 번째 run만 변경, 나머지 제거
    meta_cell = table1.rows[0].cells[1]
    for p in meta_cell.paragraphs:
        runs = p.runs
        if runs:
            runs[0].text = proj['title']
            for r in runs[1:]:
                r.text = ""

    # 연구기간 (행1, 셀1) — 첫 번째 run만 변경
    period_cell = table1.rows[1].cells[1]
    for p in period_cell.paragraphs:
        runs = p.runs
        if runs:
            runs[0].text = proj['period']
            for r in runs[1:]:
                r.text = ""

    # === 테이블 2: 연구목표 ===
    table2 = doc.tables[2]
    content_cell = table2.rows[1].cells[0]
    clear_cell_content(content_cell)
    add_text_to_cell(content_cell, content["연구목표"])

    # === 테이블 3: 실험/개발 내용 ===
    table3 = doc.tables[3]
    content_cell = table3.rows[1].cells[0]
    clear_cell_content(content_cell)
    add_text_to_cell(content_cell, content["실험개발"])

    # === 테이블 4: 결과 및 데이터 ===
    table4 = doc.tables[4]
    content_cell = table4.rows[1].cells[0]
    clear_cell_content(content_cell)
    add_text_to_cell(content_cell, content["결과데이터"])

    # === 테이블 5: 고찰 및 분석 ===
    table5 = doc.tables[5]
    content_cell = table5.rows[1].cells[0]
    clear_cell_content(content_cell)
    add_text_to_cell(content_cell, content["고찰분석"])

    # === 테이블 6: 서명 (변경 불필요, 이미 김준형/조한종) ===

    # 저장
    output_path = os.path.join(OUTPUT_DIR, f"연구노트-{project_num}-{proj['name']}_new.docx")
    doc.save(output_path)
    print(f"생성 완료: {output_path}")
    return output_path


